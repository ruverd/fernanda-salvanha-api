Route::middleware('auth:api')->get('/user', function (Request $request) {
    return $request->user();
});

Route::group([

    'middleware' => 'api',
    'namespace' => 'Modules\Auth\Http\Controllers',
    'prefix' => 'auth'

], function ($router) {

    Route::post('login', 'AuthController@login');
    Route::post('logout', 'AuthController@logout');
    Route::post('refresh', 'AuthController@refresh');
    Route::post('me', 'AuthController@me');
});


Route::apiResources([
    'users' => 'Modules\User\Http\Controllers\UserController',
    'packages' => 'Modules\Package\Http\Controllers\PackageController',
    'sessions' => 'Modules\Package\Http\Controllers\SessionController',
]);

